    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
       if(inputValidate()){
           vaccine.setVaccineID(vaccineID);
           vaccine.setBalance(Integer.parseInt(txtBalance.getText()));
           vaccine.setExpDate(dateExp.getDate());
           vaccine.setVaccineName(cboName.getSelectedItem().toString());
           vaccine.setCentreID(cboCentre.getSelectedIndex() + 1);
           
           if(mode == AccessMode.EDIT){
               vaccine.updateVaccine();
           }else if(mode == AccessMode.NEW){
               vaccine.addVaccine();
               
           }
           
           this.dispose();
       }
    }//GEN-LAST:event_btnSaveActionPerformed

    private boolean inputValidate(){
        String balance = txtBalance.getText();
        String appointAmt = txtAppointed.getText();
        
        boolean result = true;
        String message = "";
        
        if(!UIUtils.isInteger(balance)){
            result = false;
            message = "Balance must be a number";
        }
        
        if(UIUtils.isInteger(balance)){
            if(Integer.parseInt(balance) < Integer.parseInt(appointAmt)){
                result = false;
                message = "Balance cannot be less than appointed amount. Please revise the stock";
            }
            
            if(mode == AccessMode.NEW && Integer.parseInt(balance) == 0){
                result = false;
                message = "Balance invalid";
            }
        }
        
        Date expDate = dateExp.getDate();
        
        if(expDate == null){
            result = false;
            message = "Invalid Expired Date";
        }
         
        if(mode == AccessMode.NEW && expDate != null){
            Date today = new Date();
            if(!UIUtils.isToday(expDate) && expDate.before(today) ){
                result = false;
                message = "Invalid Expired Date"; 
            }
           
        }
        
        
        if(!result){
            JOptionPane.showMessageDialog(null, message, "Invalid Input", JOptionPane.WARNING_MESSAGE);
        }
        
        return result;
    }
