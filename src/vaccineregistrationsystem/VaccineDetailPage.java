    private void restrictEdit(){
        // Once the vaccine has been used for appointment cannot chg expire date and centre
        if(vaccine.getAppointedAmt() > 0){
            cboCentre.setEnabled(false);
            dateExp.setEnabled(false);
            cboName.setEnabled(false);
        
            ((JTextFieldDateEditor)dateExp.getDateEditor())
                .setDisabledTextColor(Color.darkGray);
            UIManager.put( "ComboBox.disabledForeground", Color.BLACK );

        }
    }
    
    private void displayDetails(){
        vaccine = Vaccine.searchVaccine(vaccineID);
        
        int centreID = vaccine.getCentreID();
        
        cboName.setSelectedItem(vaccine.getVaccineName());
        txtBalance.setText(String.valueOf(vaccine.getBalance()));
        txtAppointed.setText(String.valueOf(vaccine.getAppointedAmt()));
        cboCentre.setSelectedItem(Centre.searchByID(centreID).getCentreName());
        dateExp.setDate(vaccine.getExpDate());
        
        
    }
    
    private void enterViewMode(){
        cboName.setEnabled(false);
        txtBalance.setEditable(false);
        cboCentre.setEnabled(false);
        dateExp.setEnabled(false);
        
        ((JTextFieldDateEditor)dateExp.getDateEditor())
                .setDisabledTextColor(Color.darkGray);
        UIManager.put( "ComboBox.disabledForeground", Color.BLACK );
        
        btnSave.setVisible(false);
    }

