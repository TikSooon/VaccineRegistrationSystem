    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if(validateInput()){
            //Save
            people.setUserID(peopleID);
            people.setName(txtName.getText());
            people.setNric(txtIC.getText());
            people.setPasswd(txtPasswd.getText());
            people.setPhoneNo(txtPhoneNo.getText());
            people.setPostCode(Integer.parseInt(txtPostCode.getText()));
            String address = UIUtils.encodeMultiLineText(txtAddress.getText());
            people.setAddress(address);
            people.setCitizenship(chkCitizen.isSelected());
            
            if(mode == AccessMode.EDIT){
                people.update();
            }else{
                people.register();
            }
            
            this.dispose();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnGeneratePasswdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGeneratePasswdActionPerformed
        String name = txtName.getText();
        
        String phoneNo = txtPhoneNo.getText();
        
        if(name.trim().equals("") || phoneNo.trim().equals("")){
                        JOptionPane.showMessageDialog(null, "Require Name and Phone No to generate password", 
                                "Invalid Input", JOptionPane.WARNING_MESSAGE);

            return;
        }
        
        String surname = name.split(" ")[0];
        String password = surname + phoneNo;
        
        txtPasswd.setText(password);
        
    }//GEN-LAST:event_btnGeneratePasswdActionPerformed

