    private boolean validateInput(){
        String message = "";
        boolean valid = true;
        
        String password = txtPasswd.getText();
        String phoneNo = txtPhoneNo.getText();
        String postCode = txtPostCode.getText();
        String ic = txtIC.getText();
        String name = txtName.getText();
        String add = txtAddress.getText();
        
        if(UIUtils.isInputBlank(ic)){
            message = "IC/Passport No cannot be blank";
            valid = false;
        }
        
        if(UIUtils.isInputBlank(name)){
            message = "Name cannot be blank";
            valid = false;
        }
        
        if(UIUtils.isInputBlank(add)){
            message = "Address cannot be blank";
            valid = false;
        }
        
        if(!UIUtils.checkPasswordValid(password)){
            message = "Invalid Password. Minimum password length is 6";
            valid = false;
        }
        
        if(!UIUtils.checkPostcodeValid(postCode)){
            message = "Invalid PostCode";
            valid = false;
        }
        
        if(!UIUtils.checkPhoneNumberValid(phoneNo)){
            message = "Invalid Phone Number";
            valid = false;
        }
        
        boolean icduplicated;
        
        if(mode == AccessMode.EDIT){
            icduplicated = People.checkIfICExisted(ic,peopleID);
        }else{
            icduplicated = People.checkIfICExisted(ic);
        }
        
        if(icduplicated){
            message = "IC/Passport Number Existed";
            valid = false;
        }
        
        if(!valid){
            JOptionPane.showMessageDialog(null, message, "Invalid Input", JOptionPane.WARNING_MESSAGE);
        }
        
        return valid;
    }
    
