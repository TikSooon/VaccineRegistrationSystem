     private int getSelectedVaccineID(){
        int column = 0;
        int row = tableVac.getSelectedRow();
        
        if(row != -1){
            String value = tableVac.getModel().getValueAt(row, column).toString();
            return Integer.parseInt(value);
        }else{
            JOptionPane.showMessageDialog(null, "Please select a vaccine first");
        }
        return -1;    
    }
