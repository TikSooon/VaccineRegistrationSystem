    private void displayData(){
        DefaultTableModel model = (DefaultTableModel) tableVac.getModel();
        ArrayList<Vaccine> vaccineList = Vaccine.searchAvailableVaccinebyCentreID(centreID);
  
       model.setRowCount(0);
       
       for(int i = 0; i < vaccineList.size() ; i++){
         Vaccine vac = vaccineList.get(i);
         String ID = String.valueOf(vac.getVaccineID());
         String name = vac.getVaccineName();
         String netbal = String.valueOf(vac.getNetBalance());
         String expDate = UIUtils.formatDate(vac.getExpDate());
         
         model.addRow(new Object[]{ID,name,netbal,expDate});
         
       }
       
        tableVac.setModel(model);
    
    }

