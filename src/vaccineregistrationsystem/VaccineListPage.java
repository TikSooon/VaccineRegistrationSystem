    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int confirmDelete = JOptionPane.showConfirmDialog(null, "Confirm delete this vaccine record");
        if(confirmDelete == JOptionPane.OK_OPTION){
            int vaccineID = getSelectedVaccineID();
            boolean result = Vaccine.deleteVaccine(vaccineID);
            if(result){
                JOptionPane.showMessageDialog(null, "Vaccine record has been deleted");
            }else{
                JOptionPane.showMessageDialog(null, 
                        "Deletion Failed. Vaccine has been used in appointment",
                        "Deletetion Failed", 
                        JOptionPane.ERROR_MESSAGE);

            }
            displayData();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private int getSelectedVaccineID(){
        int column = 0;
        int row = tableVaccine.getSelectedRow();
        
        if(row != -1){
            String value = tableVaccine.getModel().getValueAt(row, column).toString();
            return Integer.parseInt(value);
        }else{
            JOptionPane.showMessageDialog(null, "Please select a vaccine first");
        }
        return -1;
        
    }
