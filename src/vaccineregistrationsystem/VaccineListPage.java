    private void displayData(){
        DefaultTableModel model = (DefaultTableModel) tableVaccine.getModel();
      
        model.setRowCount(0);
        
        if(searchDate == null){
            if(centreID != -1){
               vaccineList = Vaccine.searchVaccinebyCentreID(centreID);
            }
            
            if(vaccineName != null){
               vaccineList = Vaccine.searchVaccine(vaccineName);
            }
        }else{
            if(centreID != -1){
               vaccineList = Vaccine.searchVaccine(searchDate, centreID);
            }
            
            if(vaccineName != null){
               vaccineList = Vaccine.searchVaccine(searchDate, vaccineName);
            }
        }
       
        for(int i = 0; i < vaccineList.size() ; i++){
            Vaccine vaccine = vaccineList.get(i);
            String ID = String.valueOf(vaccine.getVaccineID());
            String balance = String.valueOf(vaccine.getBalance());
            String netbalance = String.valueOf(vaccine.getNetBalance());
            String expDate = UIUtils.formatDate(vaccine.getExpDate());
            String vaccineName = vaccine.getVaccineName();
            
            model.addRow(new Object[]{ID,vaccineName,balance, netbalance, expDate});
        }
        tableVaccine.setModel(model);
    }

